using System;

// Demonstrates text-based data types in C#
class TextDataTypes
{
    static void Main()
    {
        // Character type
        char singleCharacter = 'A';
        Console.WriteLine("Char: " + singleCharacter);

        // String type
        string message = "Hello, World!";
        Console.WriteLine("String: " + message);
    }
}