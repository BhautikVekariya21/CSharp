// File: NumericDataTypes.cs
using System;

// Demonstrates numeric data types in C#
class NumericDataTypes
{
    static void Main()
    {
        // Integer type
        int intNumber = 42;
        Console.WriteLine("Integer: " + intNumber);

        // Floating-point type
        float floatNumber = 3.14f;
        Console.WriteLine("Float: " + floatNumber);

        // Double type
        double doubleNumber = 3.14159;
        Console.WriteLine("Double: " + doubleNumber);

        // Decimal type
        decimal decimalNumber = 3.14159265359m;
        Console.WriteLine("Decimal: " + decimalNumber);

        // Long type
        long longNumber = 123456789L;
        Console.WriteLine("Long: " + longNumber);
    }
}